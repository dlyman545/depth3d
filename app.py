def export_obj(vertices, colors, faces, stem: str) -> bytes:
    """Export mesh as Wavefront OBJ + MTL with per-vertex colors (via texture)."""
    lines = [f"# Generated by img3d converter", f"mtllib {stem}.mtl", ""]
    for v, c in zip(vertices, colors):
        lines.append(f"v {v[0]:.5f} {v[1]:.5f} {v[2]:.5f} {c[0]/255:.4f} {c[1]/255:.4f} {c[2]/255:.4f}")
    lines.append("")
    for f in faces:
        lines.append(f"f {f[0]+1} {f[1]+1} {f[2]+1}")
    obj_bytes = "\n".join(lines).encode()

    mtl = f"newmtl default\nKa 1 1 1\nKd 1 1 1\n"
    mtl_bytes = mtl.encode()

    buf = io.BytesIO()
    with zipfile.ZipFile(buf, 'w', zipfile.ZIP_DEFLATED) as zf:
        zf.writestr(f"{stem}.obj", obj_bytes)
        zf.writestr(f"{stem}.mtl", mtl_bytes)
    return buf.getvalue()


def export_ply(vertices, colors, faces) -> bytes:
    """Export mesh as binary PLY."""
    nv, nf = len(vertices), len(faces)
    header = (
        f"ply\nformat binary_little_endian 1.0\n"
        f"element vertex {nv}\n"
        f"property float x\nproperty float y\nproperty float z\n"
        f"property uchar red\nproperty uchar green\nproperty uchar blue\n"
        f"element face {nf}\n"
        f"property list uchar int vertex_indices\n"
        f"end_header\n"
    ).encode()

    vdata = bytearray()
    for v, c in zip(vertices, colors):
        vdata += struct.pack('<fff', *v)
        vdata += struct.pack('<BBB', *c)

    fdata = bytearray()
    for f in faces:
        fdata += struct.pack('<B', 3)
        fdata += struct.pack('<iii', *f)

    return header + bytes(vdata) + bytes(fdata)


def export_stl(vertices, faces) -> bytes:
    """Export mesh as binary STL (no color, geometry only)."""
    buf = bytearray(80)  # header
    buf += struct.pack('<I', len(faces))
    for f in faces:
        v0, v1, v2 = vertices[f[0]], vertices[f[1]], vertices[f[2]]
        edge1 = v1 - v0; edge2 = v2 - v0
        n = np.cross(edge1, edge2)
        ln = np.linalg.norm(n)
        n = n / ln if ln > 0 else n
        buf += struct.pack('<fff', *n)
        buf += struct.pack('<fff', *v0)
        buf += struct.pack('<fff', *v1)
        buf += struct.pack('<fff', *v2)
        buf += struct.pack('<H', 0)
    return bytes(buf)


def export_gltf(vertices, colors, faces, stem: str) -> bytes:
    """Export as glTF 2.0 JSON + binary buffer, zipped."""
    verts_flat = vertices.astype(np.float32).flatten()
    colors_norm = (colors.astype(np.float32) / 255.0).flatten()
    faces_flat = faces.astype(np.uint32).flatten()

    v_bytes = verts_flat.tobytes()
    c_bytes = colors_norm.tobytes()
    f_bytes = faces_flat.tobytes()

    # Pad to 4-byte boundary
    def padded(b): return b + b'\x00' * (-len(b) % 4)

    v_bytes = padded(v_bytes); v_len = len(v_bytes)
    c_bytes = padded(c_bytes); c_len = len(c_bytes)
    f_bytes = padded(f_bytes); f_len = len(f_bytes)

    bin_data = v_bytes + c_bytes + f_bytes

    vmin = vertices.min(axis=0).tolist()
    vmax = vertices.max(axis=0).tolist()

    gltf = {
        "asset": {"version": "2.0", "generator": "img3d-converter"},
        "scene": 0,
        "scenes": [{"nodes": [0]}],
        "nodes": [{"mesh": 0}],
        "meshes": [{
            "name": stem,
            "primitives": [{
                "attributes": {"POSITION": 0, "COLOR_0": 1},
                "indices": 2,
                "mode": 4
            }]
        }],
        "accessors": [
            {
                "bufferView": 0, "componentType": 5126, "count": len(vertices),
                "type": "VEC3", "min": vmin, "max": vmax
            },
            {
                "bufferView": 1, "componentType": 5126, "count": len(vertices),
                "type": "VEC3"
            },
            {
                "bufferView": 2, "componentType": 5125, "count": len(faces) * 3,
                "type": "SCALAR"
            }
        ],
        "bufferViews": [
            {"buffer": 0, "byteOffset": 0, "byteLength": v_len, "target": 34962},
            {"buffer": 0, "byteOffset": v_len, "byteLength": c_len, "target": 34962},
            {"buffer": 0, "byteOffset": v_len + c_len, "byteLength": f_len, "target": 34963},
        ],
        "buffers": [{"byteLength": len(bin_data), "uri": f"{stem}.bin"}]
    }

    buf = io.BytesIO()
    with zipfile.ZipFile(buf, 'w', zipfile.ZIP_DEFLATED) as zf:
        zf.writestr(f"{stem}.gltf", json.dumps(gltf, indent=2))
        zf.writestr(f"{stem}.bin", bin_data)
    return buf.getvalue()


def export_xyz(vertices, colors) -> bytes:
    """Export as ASCII point cloud XYZ with RGB."""
    lines = []
    for v, c in zip(vertices, colors):
        lines.append(f"{v[0]:.5f} {v[1]:.5f} {v[2]:.5f} {c[0]} {c[1]} {c[2]}")
    return "\n".join(lines).encode()


