# ─────────────────────────────────────────────
# Depth Estimation (pure NumPy, no ML required)
# ─────────────────────────────────────────────

def estimate_depth_map(img_array: np.ndarray) -> np.ndarray:
    """
    Heuristic depth estimation from a single image.
    Uses luminance, edge detection, and vertical position bias
    (objects lower in frame tend to be closer — the "ground plane" assumption).
    """
    if img_array.ndim == 3 and img_array.shape[2] == 4:
        img_array = img_array[:, :, :3]

    # Luminance
    lum = (0.299 * img_array[:,:,0] + 0.587 * img_array[:,:,1] + 0.114 * img_array[:,:,2]) / 255.0

    # Laplacian edge magnitude (focus measure)
    def laplacian(arr):
        k = np.array([[0,-1,0],[-1,4,-1],[0,-1,0]], dtype=np.float32)
        from scipy.ndimage import convolve
        return np.abs(convolve(arr.astype(np.float32), k))

    try:
        from scipy.ndimage import convolve, gaussian_filter
        edges = laplacian(lum)
        edges = gaussian_filter(edges, sigma=2)
        smooth_lum = gaussian_filter(lum, sigma=3)
    except ImportError:
        edges = np.zeros_like(lum)
        smooth_lum = lum

    # Vertical gradient bias (top = far, bottom = near)
    h, w = lum.shape
    y_grad = np.linspace(0.2, 0.8, h)[:, None] * np.ones((1, w))

    # Combine cues
    depth = (0.4 * smooth_lum + 0.3 * (1.0 - edges / (edges.max() + 1e-8)) + 0.3 * y_grad)

    # Normalize to [0, 1]
    depth = (depth - depth.min()) / (depth.max() - depth.min() + 1e-8)
    return depth.astype(np.float32)


def depth_to_mesh(depth: np.ndarray, img: np.ndarray, scale_z: float = 0.4,
                  subsample: int = 2):
    """
    Convert a depth map + color image to a triangulated mesh.
    Returns vertices (N,3), colors (N,3 uint8), faces (M,3).
    """
    h, w = depth.shape
    # Subsample for manageable mesh size
    ys = np.arange(0, h, subsample)
    xs = np.arange(0, w, subsample)
    gy, gx = np.meshgrid(ys, xs, indexing='ij')

    sh, sw = gy.shape

    # Normalised XY coords [-1, 1]
    nx = (gx / (w - 1)) * 2 - 1
    ny = (gy / (h - 1)) * 2 - 1
    nz = depth[gy, gx] * scale_z

    vertices = np.stack([nx, -ny, nz], axis=-1).reshape(-1, 3).astype(np.float32)

    # Colors
    if img.ndim == 3 and img.shape[2] == 4:
        img = img[:, :, :3]
    colors = img[gy, gx].reshape(-1, 3).astype(np.uint8)

    # Faces — two triangles per quad
    def idx(r, c): return r * sw + c
    faces = []
    for r in range(sh - 1):
        for c in range(sw - 1):
            a, b, c_, d = idx(r,c), idx(r,c+1), idx(r+1,c), idx(r+1,c+1)
            faces.append([a, b, c_])
            faces.append([b, d, c_])
    faces = np.array(faces, dtype=np.int32)

    return vertices, colors, faces


