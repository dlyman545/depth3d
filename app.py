@app.route('/convert', methods=['POST'])
def convert():
    try:
        if 'image' not in request.files:
            return jsonify({"error": "No image file provided"}), 400

        file = request.files['image']
        fmt = request.form.get('format', 'ply').lower()
        quality = request.form.get('quality', 'medium').lower()

        quality_map = {'low': 4, 'medium': 2, 'high': 1}
        subsample = quality_map.get(quality, 2)

        scale_z = float(request.form.get('depth_scale', '0.4'))

        # Load image
        img_pil = Image.open(file.stream).convert('RGBA')

        # Resize for processing (keep aspect, max 512px on longest side)
        max_dim = 512
        w, h = img_pil.size
        if max(w, h) > max_dim:
            scale = max_dim / max(w, h)
            img_pil = img_pil.resize((int(w*scale), int(h*scale)), Image.LANCZOS)

        img_array = np.array(img_pil)
        img_rgb = img_array[:, :, :3]

        logger.info(f"Processing image {img_pil.size}, format={fmt}, quality={quality}")

        # Depth estimation
        depth = estimate_depth_map(img_array)

        # Mesh generation
        vertices, colors, faces = depth_to_mesh(depth, img_rgb, scale_z=scale_z, subsample=subsample)

        stem = f"model_{uuid.uuid4().hex[:8]}"

        # Export
        if fmt == 'ply':
            data = export_ply(vertices, colors, faces)
            mime = 'application/octet-stream'
            filename = f"{stem}.ply"
        elif fmt == 'obj':
            data = export_obj(vertices, colors, faces, stem)
            mime = 'application/zip'
            filename = f"{stem}_obj.zip"
        elif fmt == 'stl':
            data = export_stl(vertices, faces)
            mime = 'application/octet-stream'
            filename = f"{stem}.stl"
        elif fmt == 'gltf':
            data = export_gltf(vertices, colors, faces, stem)
            mime = 'application/zip'
            filename = f"{stem}_gltf.zip"
        elif fmt == 'xyz':
            data = export_xyz(vertices, colors)
            mime = 'text/plain'
            filename = f"{stem}.xyz"
        else:
            return jsonify({"error": f"Unknown format: {fmt}"}), 400

        stats = {
            "vertices": len(vertices),
            "faces": len(faces),
            "image_size": list(img_pil.size),
            "format": fmt,
        }
        logger.info(f"Export complete: {stats}")

        resp = send_file(
            io.BytesIO(data),
            mimetype=mime,
            as_attachment=True,
            download_name=filename
        )
        resp.headers['X-Mesh-Stats'] = json.dumps(stats)
        return resp

    except Exception as e:
        logger.error(traceback.format_exc())
        return jsonify({"error": str(e)}), 500


